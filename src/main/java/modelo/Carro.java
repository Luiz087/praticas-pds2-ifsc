package modelo;

public class Carro {

	private int idCarro;
	private String modeloCarro;

	public int getIdCarro() {
		return idCarro;
	}

	public void setIdCarro(int id_carro) {
		this.idCarro = id_carro;
	}

	public String getModeloCarro() {
		return modeloCarro;
	}

	public void setModeloCarro(String modelo_carro) {
		this.modeloCarro = modelo_carro;
	}

}
